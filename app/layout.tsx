import "./css/style.css";

import { Roboto } from "next/font/google";

import Header from "@/components/ui/header";
import CookieBanner from "@/components/ui/cookie-banner";
import {
  COOKIE_KEY,
  DEFAULT_THEME,
  ThemeProvider,
  STORAGE_KEY,
} from "@/components/theme-provider";

const roboto = Roboto({
  subsets: ["latin", "cyrillic"],
  weight: ["400", "500", "700"],
  style: ["normal", "italic"],
  variable: "--font-roboto",
  display: "swap",
});

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html lang="ru" data-theme={DEFAULT_THEME} suppressHydrationWarning>
      <body
        className={`${roboto.variable} font-inter text-base antialiased`}
      >
        <script
          dangerouslySetInnerHTML={{
            __html: `(function(){var theme='${DEFAULT_THEME}';try{var match=document.cookie.match(/(?:^|;\\s*)${COOKIE_KEY}=(light|dark)(?:;|$)/);if(match){theme=match[1];}else{var local=localStorage.getItem('${STORAGE_KEY}');if(local==='light'||local==='dark'){theme=local;}}}catch(e){}document.documentElement.dataset.theme=theme;document.documentElement.style.colorScheme=theme;})();`,
          }}
        />
        <ThemeProvider>
          <div className="site-shell flex min-h-screen flex-col overflow-hidden pt-24 md:pt-28 supports-[overflow:clip]:overflow-clip">
            <Header />
            {children}
          </div>
          <CookieBanner />
        </ThemeProvider>
      </body>
    </html>
  );
}
